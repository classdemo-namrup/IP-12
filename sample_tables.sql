CREATE TABLE INVENTORY(
	CarId CHAR(4) PRIMARY KEY,
	CarName VARCHAR(20),
	Price FLOAT,
	Model VARCHAR(20),
	YearManufacture SMALLINT,
	Fueltype VARCHAR(20)
);

CREATE TABLE CUSTOMER(
	CustId CHAR(5) PRIMARY KEY,
	CustName VARCHAR(20),
	CustAdd VARCHAR(100),
	Phone CHAR(10),
	Email VARCHAR(50)
);

CREATE TABLE SALE(
	InvoiceNo CHAR(6) PRIMARY KEY,
	CarId CHAR(4) REFERENCES INVENTORY(CarId),
	CustId CHAR(5) REFERENCES CUSTOMER(CustId),
	SaleDate DATE,
	PaymentMode VARCHAR(20),
	EmpID CHAR(4) REFERENCES EMPLOYEE(EmpId),
	SalePrice FLOAT
);

CREATE TABLE Employee(
EmpID CHAR(4),
EmpName VARCHAR(20),
DOB DATE,
DOJ DATE,
Designation VARCHAR(20),
Salary FLOAT
);

